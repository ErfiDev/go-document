package main

import "fmt"

// Data types in golang
func main() {
	// Integer types
	var age int = 27

	// Floating types
	var pi float64 = 3.14

	// String types
	var name string = "erfan"

	// Boolean types
	var human bool = true

	fmt.Println("my age: ", age)
	fmt.Println("my name: ", name)
	fmt.Println("im a human? ", human)
	fmt.Println("pi number: ", pi)
}
